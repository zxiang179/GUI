package project.RealEstateAgency.model;

import java.util.Date;

public class Register {
	private String phone;
	private Date date;

	public Register() {

	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}

}
